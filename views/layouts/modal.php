    <h3><b>What is Social Distancing?</b></h3><p align="justify">
      
Social distancing, or physical distancing, is a set of non-pharmaceutical interventions or measures taken to prevent the spread of a contagious disease by maintaining a physical distance between people and reducing the number of times people come into close contact with each other 
Maintain at least 1 metre (3 feet) distance between yourself and anyone who is coughing or sneezing. <br>
Why? When someone coughs or sneezes they spray small liquid droplets from their nose or mouth which may contain virus. If you are too close, you can breathe in the droplets, including the COVID-19 virus if the person coughing has the disease. 


    </p><h3><b>What to do if exposed to COVID-19?</b></h3>
    <p>
Anyone who has:<br>
1)  Returned from outside Nigeria and/or <br>  
2)  Come in contact with a confirmed case of coronavirus <br>
Should self-isolate i.e. stay home and avoid contact with people including their families for 14 days. Do not go to work, school or public places and avoid public transportation. Avoid self-medication.
 if you develop respiratory symptoms such as fever, cough or difficulty breathing.
